package br.ufrj.coppe.pesc.ratatouille.exception;

public class ImpossivelAtualizarFrequenciaAlimentoException extends Exception {

	private static final long serialVersionUID = 4501835461046842462L;



	public ImpossivelAtualizarFrequenciaAlimentoException() {
	}



	public ImpossivelAtualizarFrequenciaAlimentoException(String message) {
		super(message);
	}



	public ImpossivelAtualizarFrequenciaAlimentoException(Throwable cause) {
		super(cause);
	}



	public ImpossivelAtualizarFrequenciaAlimentoException(String message, Throwable cause) {
		super(message, cause);
	}



	public ImpossivelAtualizarFrequenciaAlimentoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
