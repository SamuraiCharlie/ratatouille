package br.ufrj.coppe.pesc.ratatouille.exception;

public class ImpossivelReunirInformacoesAlimentosException extends Exception {

	private static final long serialVersionUID = -2350591211843992987L;



	public ImpossivelReunirInformacoesAlimentosException() {
	}



	public ImpossivelReunirInformacoesAlimentosException(String message) {
		super(message);
	}



	public ImpossivelReunirInformacoesAlimentosException(Throwable cause) {
		super(cause);
	}



	public ImpossivelReunirInformacoesAlimentosException(String message, Throwable cause) {
		super(message, cause);
	}



	public ImpossivelReunirInformacoesAlimentosException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
